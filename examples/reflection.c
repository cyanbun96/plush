uint8_t blank[BLANK_WIDTH*BLANK_HEIGHT];
uint8_t reflection_framebuffer[REFLECTION_WIDTH*REFLECTION_HEIGHT];
float reflection_zbuffer[REFLECTION_WIDTH*REFLECTION_HEIGHT];
pl_Cam *reflection_camera;
pl_Texture reflection_texture;
pl_Texture blank_texture;

int main(int argc, char **argv)
{
	size_t i;
	float moon_ang = 0;
	float camera_ang = ((2*PL_PI) / 4) * 3;

	/* setup graphics mode */
	exSetGraphics();

	/* setup textures */
	/* 72 is a nice light blue color in std.pal */
	plMemSet(blank, 68, sizeof(blank));
	plMemSet(reflection_framebuffer, 0, sizeof(reflection_framebuffer));

	/* setup materials */
	for (i = 0; i < NUM_MATERIALS; i++)
	{
		materials[i] = plMatCreate();
		materials[i]->Ambient[0] = materials[i]->Ambient[1] = materials[i]->Ambient[2] = -128;
		materials[i]->Specular[0] = materials[i]->Specular[1] = materials[i]->Specular[2] = 64;
		materials[i]->Diffuse[0] = materials[i]->Diffuse[1] = materials[i]->Diffuse[2] = 128;
		materials[i]->NumGradients = 2500;
		materials[i]->ShadeType = PL_SHADE_GOURAUD;
	}

	/* hack up a blank texture */
	blank_texture.Data = blank;
	blank_texture.PaletteData = palette;
	blank_texture.NumColors = 256;
	blank_texture.Width = 7;
	blank_texture.Height = 7;
	blank_texture.iWidth = BLANK_WIDTH;
	blank_texture.iHeight = BLANK_HEIGHT;
	blank_texture.uScale = 128;
	blank_texture.vScale = 128;

	/* hack up a texture for the reflection framebuffer */
	reflection_texture.Data = reflection_framebuffer;
	reflection_texture.PaletteData = palette;
	reflection_texture.NumColors = 256;
	reflection_texture.Width = 7;
	reflection_texture.Height = 7;
	reflection_texture.iWidth = REFLECTION_WIDTH;
	reflection_texture.iHeight = REFLECTION_HEIGHT;
	reflection_texture.uScale = 128;
	reflection_texture.vScale = 128;

	/* mirrored teapot */
	materials[0]->Ambient[0] = materials[0]->Ambient[1] = materials[0]->Ambient[2] = 0;
	materials[0]->Texture = &blank_texture;
	materials[0]->Environment = &reflection_texture;
	materials[0]->TexEnvMode = PL_TEXENV_ADD;

	/* earth */
	materials[1]->Texture = plReadPCXTex("earth.pcx", true, true);

	/* moon */
	materials[2]->Texture = plReadPCXTex("moon.pcx", true, true);

	/* sky */
	materials[3]->Ambient[0] = materials[3]->Ambient[1] = materials[3]->Ambient[2] = 0;
	materials[3]->ShadeType = PL_SHADE_NONE;
	materials[3]->Shininess = 1;
	materials[3]->Texture = plReadPCXTex("sky2.pcx", true, true);
	materials[3]->TexScaling = 20.0;
	materials[3]->PerspectiveCorrect = 2;

	/* initialize materials */
	for (i = 0; i < NUM_MATERIALS; i++)
		plMatInit(materials[i]);

	/* map materials to palette */
	for (i = 0; i < NUM_MATERIALS; i++)
		plMatMapToPal(materials[i], palette, 0, 255);

	/* set screen palette */
	exSetPalette(palette);

	/* load teapot model */
	teapot = plObjCreate(NULL);
	teapot->Xa = 15;
	teapot->Ya = 10;
	teapot->Model = plReadWavefrontMdl("teapot.obj", materials[0]);
	plMdlScale(teapot->Model, 48);
	plMdlTranslate(teapot->Model, 0, -48, 0);

	/* create earth */
	earth = plObjCreate(NULL);
	earth->Model = plMakeSphere(128, 10, 10, materials[1]);
	earth->Xp -= 384;

	/* create moon */
	moon = plObjCreate(NULL);
	moon->Model = plMakeSphere(64, 10, 10, materials[2]);
	moon->Xp += 384;

	/* create sky */
	sky = plObjCreate(NULL);
	sky->Model = plMakeSphere(65000, 10, 10, materials[3]);
	plMdlFlipNormals(sky->Model);

	/* create camera */
	camera = plCamCreate(W, H, W * 3.0 / (H * 4.0), 100.0, framebuffer, zbuffer);
	camera->Z = -CAMERA_DIST;

	/* create reflection camera */
	reflection_camera = plCamCreate(REFLECTION_WIDTH, REFLECTION_HEIGHT, 1.0, 150.0, reflection_framebuffer, reflection_zbuffer);
	reflection_camera->Pitch = 5;
	reflection_camera->Pan = 90;
	reflection_camera->Roll = -10;

	/* create light */
	light = plLightSet(plLightCreate(), PL_LIGHT_VECTOR, 0.0, 0.0, 0.0, 1.0, 1.0);

	/* main loop */
	while (!exGetKey())
	{
		/* render reflection frame */
		memset(reflection_zbuffer, 0, sizeof(reflection_zbuffer));
		memset(reflection_framebuffer, 0, sizeof(reflection_framebuffer));
		plRenderBegin(reflection_camera);
		plRenderLight(light);
		plRenderObj(earth);
		plRenderObj(moon);
		plRenderObj(sky);
		plRenderEnd();

		/* rotate teapot */
		teapot->Xa += 0.05f;
		teapot->Ya -= 0.05f;
		teapot->Za += 0.05f;

		/* roate earth */
		earth->Ya += 0.25;

		/* move moon around and point at earth */
		moon->Ya = plRadToDeg(moon_ang);
		moon->Xp = earth->Xp + MOON_DIST * cos(moon_ang);
		moon->Zp = earth->Zp + MOON_DIST * sin(moon_ang);

		/* moon orbit angle */
		moon_ang += 0.015f;
		if (moon_ang >= 2*PL_PI)
			moon_ang = 0;

		/* move camera around */
		camera->X = teapot->Xp + CAMERA_DIST * cos(camera_ang);
		camera->Z = teapot->Zp + CAMERA_DIST * sin(camera_ang);
		camera->Pan = 90 + plRadToDeg(atan2(camera->Z, camera->X));

		/* camera orbit angle */
		camera_ang += 0.0005f;
		if (camera_ang >= 2*PL_PI)
			camera_ang = 0;

		/* clear back buffer */
		memset(zbuffer, 0, sizeof(zbuffer));
		memset(framebuffer, 0, sizeof(framebuffer));

		/* render frame */
		plRenderBegin(camera);
		plRenderLight(light);
		plRenderObj(teapot);
		plRenderObj(earth);
		plRenderObj(moon);
		plRenderObj(sky);
		plRenderEnd();

		/* put reflection cam in the corner */
		for (i = 0; i < REFLECTION_HEIGHT; i++)
		{
			uint8_t *src = reflection_framebuffer + i * REFLECTION_WIDTH;
			uint8_t *dst = framebuffer + i * W;
			plMemCpy(dst, src, REFLECTION_WIDTH);
		}
		plTextPutStr(camera, 0, REFLECTION_HEIGHT, 0, 217, "reflection cam");

		/* wait for vsync, then copy to screen */
		exWaitVSync();
		memcpy(exGraphMem, framebuffer, sizeof(framebuffer));
	}

	/* clean up */
	plCamDelete(camera);
	plCamDelete(reflection_camera);
	plLightDelete(light);
	plObjDelete(teapot);
	plObjDelete(earth);
	plObjDelete(moon);
	plObjDelete(sky);

	for (i = 0; i < NUM_MATERIALS; i++)
		plMatDelete(materials[i]);

	/* shut down video */
	exSetText();

	return 0;
}
